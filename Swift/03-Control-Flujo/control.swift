// Condicionales
let nota = 7

if nota >= 5 {
    print("¡Aprobado!")
} else {
    print("Suspenso.")
}

// Switch
let letra = "B"
switch letra {
case "A":
    print("Excelente")
case "B":
    print("Bien")
default:
    print("Sigue esforzándote")
}

// Bucles
for i in 1...5 {
    print("Número: \(i)")
}

var contador = 0
while contador < 3 {
    print("Contador: \(contador)")
    contador += 1
}